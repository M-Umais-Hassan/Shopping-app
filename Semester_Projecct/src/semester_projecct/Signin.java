    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        obj.setUser_Name(user.getText());
        obj.setPassword(pass.getText());
        try{
            if(!obj.getUser_Name().trim().equals("") && !obj.getPassword().trim().equals("")){  
                String strline;
                FileInputStream fstream = new FileInputStream("username.txt");
                BufferedReader br = new BufferedReader(new InputStreamReader(fstream));
                String strline1;
                FileInputStream fstream1 = new FileInputStream("password.txt");
                BufferedReader br1 = new BufferedReader(new InputStreamReader(fstream1));
                while((strline = br.readLine())!=null ){
                    if(obj.getUser_Name() == null ? strline == null : obj.getUser_Name().equals(strline)){
                        while((strline1 = br1.readLine())!=null){
                            if(obj.getPassword() == null ? strline1 == null : obj.getPassword().equals(strline1)){
                                dispose();
                                Main_Frame obj1 = new Main_Frame();
                                obj1.setVisible(true);
                            }
                        }      
                    }
                    else{
                        Message.setText("Wrong Username or password");
                    }
                }
                fstream.close();
                fstream1.close();
            }else{
                Message.setText("Username or password field should not be left blank");
            }
        }catch (HeadlessException | IOException e ){
            JOptionPane.showMessageDialog(rootPane,"Something went wrong, Please try again later");
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

