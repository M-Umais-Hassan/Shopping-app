    private void comboboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboboxActionPerformed
        Object selected = combobox.getSelectedItem();
        if(selected.toString().equals("Logout")){
            dispose();
            Signin objS = new Signin();
            objS.setVisible(true);
        }
        else if(selected.toString().equals("Contact")){
            Contact objC = new Contact();
            hide();
            objC.setVisible(true);
        }
        else if(selected.toString().equals("Admin Portal")){
            dispose();
            Admin objA = new Admin();
            objA.setVisible(true);
        }
        else if(selected.toString().equals("Complains")){
            Complains objCo = new Complains();
            dispose();
            objCo.setVisible(true);
        }
    }//GEN-LAST:event_comboboxActionPerformed

