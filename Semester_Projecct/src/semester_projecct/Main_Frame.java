    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Reading home appliances
        try{                                         
            
            BufferedReader br;
            br = new BufferedReader(new FileReader("Home Appliances.txt"));
            String firstLine = br.readLine().trim();
            DefaultTableModel model = (DefaultTableModel) obj2.Table_1.getModel();
            
            Object[] lines = br.lines().toArray();
            
            for (int i=0; i<lines.length; i++) {
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
        }catch (FileNotFoundException ex) {
            Logger.getLogger(Main_Frame2.class.getName()).log(Level.SEVERE, null, ex);
        }catch (IOException ex) {
            Logger.getLogger(Main_Frame2.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
        obj2.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

