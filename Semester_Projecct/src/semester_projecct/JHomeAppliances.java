    private void Export2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Export2ActionPerformed
        Message2.setText("");
        DefaultTableModel model = (DefaultTableModel) Table2.getModel();
        if(Table2.getSelectedRow()==-1){
            if(Table2.getSelectedColumn()==0){
                Message2.setText("Table is Empty");
            }
            else{
                Message2.setText("You must select a product");
            }
        }
        else{
            model.setValueAt(ID2.getText(),Table2.getSelectedRow(),0);
            model.setValueAt(Name2.getText(),Table2.getSelectedRow(),1);
            model.setValueAt(Company2.getText(),Table2.getSelectedRow(),2);
            model.setValueAt((int) stock.getValue(),Table2.getSelectedRow(),3);
            model.setValueAt(Price2.getText(),Table2.getSelectedRow(),4);

        }
    }//GEN-LAST:event_Export2ActionPerformed

