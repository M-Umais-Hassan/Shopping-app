    private void Export1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Export1ActionPerformed
        DefaultTableModel model = (DefaultTableModel) Table2.getModel();
        if(!Name2.getText().trim().equals("") && !ID2.getText().trim().equals("") && !Company2.getText().trim().equals("")){
            try {
                File file = new File("Home Appliances.txt");
                BufferedWriter bw = new BufferedWriter(new FileWriter(file,true));
                for(int i=0; i<Table2.getRowCount(); i++){
                    for(int j=0; j<Table2.getColumnCount(); j++){
                        bw.append((String)Table2.getModel().getValueAt(i, j).toString() + " ");
                    }
                    bw.append(System.getProperty("line.separator"));
                }
                JOptionPane.showMessageDialog(rootPane, "Successfully Inserted data");
                bw.close();
                model.setRowCount(0);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, "Something went wrong please try gain later");
            }
        }
    }//GEN-LAST:event_Export1ActionPerformed

